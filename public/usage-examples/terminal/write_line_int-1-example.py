from splashkit import *

# Example 1: Print single integer
write_line_int(1)
write_line_int(2)
write_line_int(3)
write_line_int(-1)
write_line_int(-2)
write_line_int(-3)

# Example 2: Print multi-digit integer
write_line_int(12345)
write_line_int(953221311)
write_line_int(-165746)

# Example 3: Print integer after calculation
a = 222 - 111
b = 10 * 12
c = int(100 / 5)
write_line_int(a - b)
write_line_int(b)
write_line_int(c)
